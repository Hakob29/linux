#!/bin/bash


#------------------------------------------------------------------------------
#IF INPUT VALUE NOT EQUAL 3, EXIT
#------------------------------------------------------------------------------
if [ $# -ne 3 ]; then
echo "Please enter three integers: "
exit 1
fi

#------------------------------------------------------------------------------
#INPUT VALUES TO VARIABLE
#------------------------------------------------------------------------------
num1=$1
num2=$2
num3=$3

#------------------------------------------------------------------------------
#COMPARE INPUT VALUES 
#------------------------------------------------------------------------------
if [ $num1 -eq $num2 ] && [ $num2 -eq $num3 ]; then

	echo =============
	echo "EQUILATERAL"
	echo =============

elif [ $num1 -eq $num2 ] || [ $num3 -eq $num1 ] ||  [ $num2 -eq $num3 ]; then 

  	echo =============
        echo "SCALENE"
        echo =============
	
elif [ $num1 -ne $num2 ] && [ $num2 -ne $num3 ] && [ $num1 -ne $num3 ]; then

        echo =============
        echo "ISOSCELES"
        echo =============

fi
